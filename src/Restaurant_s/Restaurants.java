    private void jbtnMealCostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnMealCostActionPerformed
        // TODO add your handling code here:
        if((jCheckBox1.isSelected()))
        {
            double cb = Double.parseDouble(jtxtChickenBurger.getText());
           
            
            cost = cb * Chicken_burger;
            String Cost = String.format("%.2f", cost);
            jtxtCostMeal.setText(Cost);
            
             
            
            
            
            
            
            
        }
        
        
      
        
        if(jCheckBox2.isSelected())
        {
            double cbm = Double.parseDouble(jtxtBurgerMeal.getText());
            cost = cbm * Chicken_burger_meal;
            String Cost = String.format("%.2f", cost);
            jtxtCostMeal.setText(Cost);
           
            
            
        }
         
        
         if(jCheckBox3.isSelected() )
        {
            double bacn = Double.parseDouble(jtxtBacon.getText());
            cost = bacn * Bacon;
            String Cost = String.format("%.2f", cost);
            jtxtCostMeal.setText(Cost);
            
           
            
        }
          
         
         if((jCheckBox1.isSelected()) && (jCheckBox2.isSelected()))
         {
             double cb = Double.parseDouble(jtxtChickenBurger.getText());
             double cbm = Double.parseDouble(jtxtBurgerMeal.getText());
             cost = ((cb*Chicken_burger)+(cbm*Chicken_burger_meal));
             String Cost = String.format("%.2f", cost);
             jtxtCostMeal.setText(Cost);
             
             
            
         }
         
         if((jCheckBox1.isSelected()) && (jCheckBox3.isSelected()))
         {
             double cb = Double.parseDouble(jtxtChickenBurger.getText());
             double bacn = Double.parseDouble(jtxtBacon.getText());
             cost = ((cb*Chicken_burger)+(bacn*Bacon));
             String Cost = String.format("%.2f", cost);
             jtxtCostMeal.setText(Cost);
             
             
            
         }
         
         if((jCheckBox2.isSelected()) && (jCheckBox3.isSelected()))
         {
             double cbm = Double.parseDouble(jtxtBurgerMeal.getText());
             double bacn = Double.parseDouble(jtxtBacon.getText());
             cost = ((cbm*Chicken_burger_meal)+(bacn*Bacon));
             String Cost = String.format("%.2f", cost);
             jtxtCostMeal.setText(Cost);
             
             
          
         }
         
         if((jCheckBox1.isSelected()) && (jCheckBox2.isSelected()) && (jCheckBox3.isSelected()))
         {
             double cbm = Double.parseDouble(jtxtBurgerMeal.getText());
             double bacn = Double.parseDouble(jtxtBacon.getText());
             double cb = Double.parseDouble(jtxtChickenBurger.getText());
             cost = ((cbm*Chicken_burger_meal)+(bacn*Bacon) + (cb*Chicken_burger));
             String Cost = String.format("%.2f", cost);
             jtxtCostMeal.setText(Cost);
             
             
            
         }
         
         
    }//GEN-LAST:event_jbtnMealCostActionPerformed

