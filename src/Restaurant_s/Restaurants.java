    private void jbtnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEqualsActionPerformed
        // TODO add your handling code here:
        secondnum = Double.parseDouble(jtxtDisplay.getText());
        if(operations == "+")
        {
            result = firstnum + secondnum;
            String add = String.valueOf(result);
            jtxtDisplay.setText(add);
            
            //answer = String.format("%.0f", result);
            //jTextField1.setText(answer);
           
           
        }
        else
            if(operations == "-")
        {
            result = firstnum - secondnum;
            String add = String.valueOf(result);
            jtxtDisplay.setText(add);
           
           
        }
        else
                if(operations == "*")
        {
            result = firstnum * secondnum;
            String add = String.valueOf(result);
            jtxtDisplay.setText(add);
           
        }
        else
                    if(operations == "/" && secondnum != 0)
        {
            
            result = firstnum / secondnum;
            String add = String.valueOf(result);
            jtxtDisplay.setText(add);
           
           
        }
                    else jtxtDisplay.setText("Error");
    }//GEN-LAST:event_jbtnEqualsActionPerformed

